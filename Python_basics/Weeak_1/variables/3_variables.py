#Multiple Variable Assignment
# Single value to multiple variables
x = y = z = 0

# Multiple values to multiple variables
name, age, city = "Bob", 25, "New York"

# Swapping variables
a = 5
b = 10
a, b = b, a  # Now a=10, b=5
print("Name:", name)
print("Age:", age)
print("City:", city)
print("a:", a)
print("b:", b)